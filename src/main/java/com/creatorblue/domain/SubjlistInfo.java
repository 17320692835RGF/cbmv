package com.creatorblue.domain;

public class SubjlistInfo {
	private Integer subjlistId;
	private String subjlistNum;
	private String subjlistName;
	private String subjlistDes;
	public Integer getSubjlistId() {
		return subjlistId;
	}
	public void setSubjlistId(Integer subjlistId) {
		this.subjlistId = subjlistId;
	}
	public String getSubjlistNum() {
		return subjlistNum;
	}
	public void setSubjlistNum(String subjlistNum) {
		this.subjlistNum = subjlistNum;
	}
	public String getSubjlistName() {
		return subjlistName;
	}
	public void setSubjlistName(String subjlistName) {
		this.subjlistName = subjlistName;
	}
	public String getSubjlistDes() {
		return subjlistDes;
	}
	public void setSubjlistDes(String subjlistDes) {
		this.subjlistDes = subjlistDes;
	}
	@Override
	public String toString() {
		return "TloglistInfo [subjlistId=" + subjlistId + ", subjlistNum=" + subjlistNum + ", subjlistName="
				+ subjlistName + ", subjlistDes=" + subjlistDes + "]";
	}
}
